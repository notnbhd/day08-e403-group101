from typing import Optional


def rerank_cross_encoder(
    query: str, candidates: list[dict], top_k: int = 5
) -> list[dict]:
    # Mocking cross encoder by simply scoring based on word overlap for test purposes,
    # or using a very light local sentence transformer.
    query_words = set(query.lower().split())
    scored = []
    for i, c in enumerate(candidates):
        content_words = set(c["content"].lower().split())
        overlap = len(query_words.intersection(content_words))
        # Add original score as tie-breaker
        new_score = overlap + c.get("score", 0) * 0.1
        scored.append({**c, "score": new_score})
    
    scored.sort(key=lambda x: x["score"], reverse=True)
    return scored[:top_k]

